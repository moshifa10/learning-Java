package org.example;

public class ArrayPrint {

    //6. Array Print
    //
    //Create an array of 5 numbers and print all elements using a loop.
    public static void main(String[] args){
        int[] numbers =  {1,2,3,4,5};

        for (int num : numbers) {
            System.out.println(num);
        }
    }
}
